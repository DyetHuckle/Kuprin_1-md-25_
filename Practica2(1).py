def combine_words(num_words):
    combined_string = ""  # Создаем пустую строку для хранения объединенных слов
    for i in range(num_words):  # Цикл для ввода и объединения слов
        word = input(f"Введите слово {i + 1}: ")  # Запрашиваем ввод слова у пользователя
        combined_string += word + " "  # Добавляем слово к объединенной строке с пробелом после каждого слова
    return combined_string.strip()  # Возвращаем объединенную строку с удалением лишних пробелов в начале и конце


# Запрашиваем количество слов у пользователя
num_words = int(input("Введите количество слов: "))

# Вызываем функцию и выводим результат
result = combine_words(num_words)
print("Объединенная строка:", result)

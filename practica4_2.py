def divide_by_user_input(dividend, divisor):
    """
    Делит число 100 на введенное пользователем число.

    :param dividend: Число, которое нужно разделить.
    :param divisor: Число, на которое нужно разделить.
    :return: Результат деления.
    """
    return dividend / divisor

try:
    # Запрашиваем ввод числа у пользователя
    user_input = input("Введите число для деления 100: ")
    # Преобразуем введенное значение в число
    divisor = float(user_input)
    # Выполняем деление и выводим результат
    result = divide_by_user_input(100, divisor)
    print(f"Результат деления 100 на {divisor} равен {result:.2f}")

except ValueError:
    # Обработка исключения, если введено не числовое значение
    print("Ошибка: Введено не числовое значение. Пожалуйста, введите число.")

except ZeroDivisionError:
    # Обработка исключения, если введено значение 0
    print("Ошибка: Деление на ноль невозможно. Пожалуйста, введите число, отличное от нуля.")

except Exception as e:
    # Обработка всех остальных исключений
    print(f"Произошла ошибка: {e}")
while True:
    # Запрашиваем слово у пользователя
    word = input("Введите слово (или 'stop' для завершения): ").strip()

    # Проверяем, введено ли слово 'stop' для завершения программы
    if word.lower() == 'stop':
        break

    # Проверяем наличие буквы 'ф' в слове
    if 'ф' in word.lower():
        print("Ого! Это редкое слово!")
    else:
        print("Эх, это не очень редкое слово...")